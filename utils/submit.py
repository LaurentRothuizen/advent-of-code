def submit(year, day, part1_result, part2_result):
    """
    Prints nicely formatted results for Part 1 and Part 2.
    
    :param year: The year of the Advent of Code challenge.
    :param day: The day of the challenge.
    :param part1_result: The result for Part 1.
    :param part2_result: The result for Part 2.
    """
    print(f"Year {year}, Day {day}")
    print(f"  Part 1: {part1_result}")
    print(f"  Part 2: {part2_result}")